# op. aritiméticos
print ('exemplos de 5 e 2 -> ')
soma = (5 + 2)
print (f' a soma é -> {soma}')
subtração = (5 - 2)
print (f' a subtração é -> {subtração}')
multip = (5 * 2)
print (f' a multiplicação é ->  {multip}')
div = (5 / 2 )
print (f' a divisão é -> {div}')
expo = (5 ** 2)
print (f' a exponenciação é -> {expo}')
divinteira = (5 // 2)
print (f' a divisão inteira é -> {divinteira}')
restodiv = (5 % 2)
print (f' o resto da divisão é -> {restodiv}')



